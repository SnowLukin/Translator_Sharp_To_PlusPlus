import Foundation

enum Operators: String, CaseIterable, Lexema {
    case plus = "+"
    case minus = "-"
    case multiply = "*"
    case divide = "/"
    case remainder = "%"
    case equal = "="
    case more = ">"
    case less = "<"
    case exclamationMark = "!"
    case singleAnd = "&"
    case singleOr = "|"
}
