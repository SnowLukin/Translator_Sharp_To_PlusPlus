import Foundation

enum Dividers: String, Lexema {
    case startBracket = "{"
    case endBracket = "}"
    case startRoundBracket = "("
    case endRoundBracket = ")"
    case comma = ","
    case semicolon = ";"
    case quotes = "\""
    case dot = "."
    case bracketLeft = "["
    case bracketRight = "]"
}
